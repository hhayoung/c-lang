#include <stdio.h>

/* 포인터와 배열의 다양한 표현방식 */
int main_p16() {

	int a[2][3] = { {1,2,3},{4,5,6} };

	// a == a[0] == &a[0] == &a[0][0] != a[0][0]; // 주소 != 값
	printf("%p %p %p %p %d\n", a, a[0], &a[0], &a[0][0], a[0][0]);

	// a[0][0] == **a == *a[0]; // 값
	// **a => *(*(a+0)+0)
	printf("%d %d %d\n", a[0][0], **a, *a[0]);

	// &a[0][1] == &a[0][0] + 1; // 주소
	printf("%p %p\n", &a[0][1], &a[0][0] + 1);

	// a + 1 == &a[1] == &a[0] + 1 == &a[1][0] != a[1][0]; // 주소 != 값
	printf("%p %p %p %p %d\n", a + 1, &a[1], &a[0] + 1, &a[1][0], a[1][0]);

	// a[1][2] == *(*(a + 1) + 2); // 값
	printf("%d %d\n", a[1][2], *(*(a + 1) + 2));

	return 0;
}